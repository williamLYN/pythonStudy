# 某公司计划购买一批价格为620元的办公桌椅，但公司批准的金额不超过20000元。
# 假设公司可以采购# 任意数量的办公椅，但每次采购的数量必须是10的倍数。
# 请计算:
# 1.这个公司最多可以采购多少张办公椅?
# 2.如果公司购买了最大数量的办公椅后，账户余额剩余多少?
# 请计算每个问题的结果。

total_mount = 20000
price = 620
number = total_mount // price // 10
res_amount = total_mount % (price * 10)
print(f'公司采购{number * 10:^10}办公椅，公司剩余的钱数{res_amount:^10}元')
